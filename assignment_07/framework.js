const lastMeasurementsRight = [];
let measurementIndexRight = 0;

function init(two) {
  // offset padding; half-pixel to get crisp lines
  two.scene.position.x = 0.5;
  two.scene.position.y = 0.5;

  const leftGroup = two.makeGroup();
  leftGroup.position.x = 10;
  leftGroup.position.y = 10;

  const rightGroup = two.makeGroup();
  rightGroup.position.x = 562;
  rightGroup.position.y = 10;

  const leftCircles = [];
  const rightCircles = [];

  [leftCircles, rightCircles].forEach(arr => {
    data.forEach(({ x, y }) => {
      const circle = new Two.Circle(x, y, 2.5);
      circle.stroke = 'none';
      circle.fill = 'grey';
      arr.push(circle);
    });
  });

  const root = initQuadtree(rightCircles, 0, 512, 0, 512);

  const rects = getQuadtreeAreas(root);
  rects.forEach(({ x0, x1, y0, y1, _ }) => {
    const rect = new Two.Rectangle(
      (x0 + x1) / 2,
      (y0 + y1) / 2,
      x1 - x0,
      y1 - y0,
    );

    rect.fill = 'none';
    rect.stroke = '#666';
    rect.addTo(rightGroup);
  });

  // add circles "above" quadtree rectangles
  leftCircles.forEach(circle => circle.addTo(leftGroup));
  rightCircles.forEach(circle => circle.addTo(rightGroup));

  const leftRadius = new Two.Circle(posX, posY, r);
  leftRadius.stroke = '#444';
  leftRadius.fill = 'none';
  const rightRadius = leftRadius.clone();

  leftRadius.addTo(leftGroup);
  rightRadius.addTo(rightGroup);

  const leftTiming = new Two.Text('?.??ms', 256, 522);
  leftTiming.addTo(leftGroup);
  const rightTiming = new Two.Text('?.??ms', 256, 522);
  rightTiming.addTo(rightGroup);

  const fps = fpsCounter("#fps")

  two.bind('update', (_, timedelta) => {

    fps.update()
    moveCircle(timedelta);

    function update(circles, radius, timing, lastMeasurements, measurementIndex, isQuadtree) {

      circles.forEach(c => c.fill = isQuadtree ? 'grey' : 'steelblue');

      radius.position.x = posX;
      radius.position.y = posY;

      const t = performance.now();
      const contained = isQuadtree
        ? quadtreeSearchAround(root, posX, posY, r)
        : findNearestCircles(circles, posX, posY, r);
      const delta = performance.now() - t;


      contained.forEach(c => c.fill = isQuadtree ? 'steelblue' : 'firebrick')
      if (isQuadtree) {
        findNearestCircles(contained, posX, posY, r).forEach(c => c.fill = 'firebrick')
      }

      if (lastMeasurements.length < numMeasurements) {
        lastMeasurements.push(delta)
      } else {
        lastMeasurements[measurementIndex] = delta;
      }

      measurementIndex = (measurementIndex + 1) % numMeasurements;

      const sum = lastMeasurements.reduce((acc, d) => acc + d, 0)
      const avg = sum / (lastMeasurements.length || 1);
      timing.value = `${isQuadtree ? "Quadtree" : "Naïve O(n)"} search: ${avg.toFixed(2)}ms`;
    }

    update(leftCircles, leftRadius, leftTiming, lastMeasurementsLeft, measurementIndexLeft, false)
    update(rightCircles, rightRadius, rightTiming, lastMeasurementsRight, measurementIndexRight, true)
  });
}

/**
 * From an array of `Two.Circle`s, return those whose center is within `radius`
 * distance of (`x`, `y`).
 *
 * @param circles: Array<Two.Circle>: Circles to filter
 * @param x: number:                  x component of coordinate
 * @param y: number:                  y component of coordinate
 * @param radius: number:             Radius within which to return circles
 *
 * @return: Array<Two.Circle>:        An array of those circles for which their
 *                                    center point is less than or equal to
 *                                    `radius` away from the coordinate
 *                                    (`x`,`y`).
 *
 */
function findNearestCircles(circles, x, y, radius) {
  // avoid expensive sqrt in loop: work with squared distances
  const dist2 = radius * radius;

  return circles.filter(circle => {
    const d2 = Math.pow(x - circle.position.x, 2) + Math.pow(y - circle.position.y, 2);
    return d2 <= dist2;
  });
}


