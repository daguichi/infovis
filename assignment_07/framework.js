let posX = 256;
let posY = 256;
let angle = 0;
const r = 50;

/**
 * Each frame, move the search circle a small amount. The circle has some
 * inertia.
 */
function moveCircle(delta = 100) {
  angle += 0.1 * (Math.random() - 0.5);
  const velocity = 2 + 2 * (delta / 100);

  let newX = posX + Math.cos(angle) * velocity;
  let newY = posY + Math.sin(angle) * velocity;

  const left = newX - r;
  const right = newX + r;
  const top = newY - r;
  const bottom = newY + r;

  if (left < 0) newX = -left + r;
  else if (right > 512) newX = 1024 - right - r;
  if (top < 0) newY = -top + r;
  else if (bottom > 512) newY = 1024 - bottom - r;

  angle = Math.atan2(newY - posY, newX - posX);

  posX = newX;
  posY = newY;

  if (mousePos) {
    posX = mousePos.x;
    posY = mousePos.y;
  }
}


// simple fps counter
function fpsCounter(selector) {

  let startTime;
  let fps = 0;
  let fpsCount = 0;

  function update() {
    if (!startTime) startTime = performance.now();
    const now = performance.now();
    const diff = now - startTime;
    fpsCount++;
    fps = fpsCount / (diff / 1000)
    if (diff >= 1000) {
      startTime = now;
      fpsCount = 0;
    }
    document.querySelector(selector).textContent = "fps: " + fps.toFixed(0);
  }

  return { update }
}

